    private void loadTable(){
        String loaiSp = null;
        String thuongHieu = null;
        String thuocTinh = null;
        loaiSp = jListLoaiSP.getSelectedValue();
        thuongHieu = jListThuongHieu.getSelectedValue();
        thuocTinh = jListThuocTinh.getSelectedValue();
        ArrayList<SanPham> listSP = thuKhoController.getSanPham(loaiSp, thuongHieu, thuocTinh);
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        for(int i=0; i<listSP.size(); i++){
            int maSP = listSP.get(i).getMaSanPham();
            String tenSP = listSP.get(i).getTenSP();
            String donVi = listSP.get(i).getDonVi();
            int donGia = listSP.get(i).getGia();
            String moTa = listSP.get(i).getMoTa();
            int soLuong = listSP.get(i).getSoLuong();
            String anhChinh = listSP.get(i).getAnhChinh();
            String anhPhu = listSP.get(i).getAnhPhu();
            Object[] rowData = {maSP, tenSP, donVi, donGia, moTa, soLuong, anhChinh, anhPhu};
            model.addRow(rowData);
        }
    }
    
