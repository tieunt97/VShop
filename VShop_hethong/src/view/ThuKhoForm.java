    private boolean checkLoaiSP(String loaiSP){
        for(int i=0; i< listLoaiSP.size(); i++){
            if(loaiSP.equalsIgnoreCase(listLoaiSP.get(i).getTenLoai())){
                return true;
            }
        }
        return false;
    }
    
    private void btnThemLSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLSPActionPerformed
        String loaiSP = JOptionPane.showInputDialog(this, "Nhập tên loại sản phẩm cần thêm !", "Thêm loại sản phẩm", JOptionPane.INFORMATION_MESSAGE);
        if(loaiSP != null){
            if(checkLoaiSP(loaiSP)){
                JOptionPane.showMessageDialog(this, "Loại sản phẩm bị trùng !\nVui lòng nhập lại !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }else{
                if(thuKhoController.themLoaiSP(loaiSP)){
                JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loaiSP();
            }else{
                JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật vào cơ sở dữ liệu", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Tên loại sản phẩm không được trống", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnThemLSPActionPerformed

    private void btnSuaLSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaLSPActionPerformed
        if(jListLoaiSP.isSelectionEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn cần chọn một loại sản phẩm muốn sửa !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String loaiSP = JOptionPane.showInputDialog(this, "Nhập tên loại sản phẩm muốn sửa !", "Sửa loại sản phẩm", JOptionPane.INFORMATION_MESSAGE);
        if(loaiSP != null){
            if(checkLoaiSP(loaiSP)){
                JOptionPane.showMessageDialog(this, "Loại sản phẩm bị trùng !\nVui lòng nhập lại !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }else{
                int maLoaiSP = listLoaiSP.get(jListLoaiSP.getSelectedIndex()).getMaLoai();
                if(thuKhoController.suaLoaiSP(loaiSP, maLoaiSP)){
                JOptionPane.showMessageDialog(this, "Sửa thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loaiSP();
            }else{
                JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật vào cơ sở dữ liệu", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Tên loại sản phẩm không được trống", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnSuaLSPActionPerformed

    private void btnXoaLSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaLSPActionPerformed
        if(jListLoaiSP.isSelectionEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn cần chọn một loại sản phẩm muốn xóa !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int ok = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa không ?", "Xóa loại sản phẩm", JOptionPane.YES_NO_OPTION);
            if(ok == 0){
                int maLoaiSP = listLoaiSP.get(jListLoaiSP.getSelectedIndex()).getMaLoai();
                if(thuKhoController.xoaLoaiSP(maLoaiSP)){
                    JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loaiSP();
                }else{
                    JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật vào cơ sở dữ liệu", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnXoaLSPActionPerformed

