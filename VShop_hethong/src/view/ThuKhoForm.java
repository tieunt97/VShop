    private void loaiSP(){
        listLoaiSP = thuKhoController.getLoaiSP();
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < listLoaiSP.size(); i++) {
            model.addElement(listLoaiSP.get(i).getTenLoai());
        }
        this.jListLoaiSP.setModel(model);
    }
    
